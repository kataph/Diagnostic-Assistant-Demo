def parse_table(raw: str) -> pd.DataFrame:
    df = pd.read_csv(io.StringIO(raw), sep="\t", header=None, names=COLS)
    return df

# ── Operations ────────────────────────────────────────────────────────────────
NUMERIC_COLS = [
    "Action_cost", "Action_cost_std",
    "Suggestion_time", "Suggestion_time_std",
    "Action_number", "Action_number_std",
    "Coverage"
]

def cell_diff(df1: pd.DataFrame, df2: pd.DataFrame) -> pd.DataFrame:
    """Return a table with:
      - key/label columns (Run, Scenario, System) from table1
      - cell-by-cell difference (table1 - table2) for all numeric columns
    """
    key_cols = ["Run", "Scenario", "System"]
    result = df1[key_cols].copy()
    for col in NUMERIC_COLS:
        result[col] = df1[col].values - df2[col].values
    return result

def cell_normalized_diff(df1: pd.DataFrame, df2: pd.DataFrame) -> pd.DataFrame:
    """Return a table with:
      - key/label columns (Run, Scenario, System) from table1
      - cell-by-cell difference (table1 - table2)/table1 for all numeric columns
    """
    key_cols = ["Run", "Scenario", "System"]
    result = df1[key_cols].copy()
    for col in NUMERIC_COLS:
        result[col] = (df1[col].values - df2[col].values)/df1[col].values
    return result

def collapse_mean_std(df: pd.DataFrame, name: str = None) -> pd.DataFrame:
    """
    Merge *_std columns into their corresponding mean columns as:
        mean ± std
    and drop the std columns.
    """
    df = df.copy()

    for col in NUMERIC_COLS:
        std_col = f"{col}_std"
        if std_col in df.columns:
            df[col] = df[col].astype(float).map("{:.2f}".format) + \
                      " ± " + \
                      df[std_col].astype(float).map("{:.2f}".format)

    # drop std columns
    df = df.drop(columns=[c for c in df.columns if c.endswith("_std")])

    if name is not None:
        df.name = name

    return df

